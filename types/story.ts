export interface Category {
  _id: string;
  name: string;
}

export interface Owner {
  _id: string;
  name: string;
  avatarUrl: string;
}

export interface Story {
  _id: string;
  title: string;
  description: string;
  img: string;
  category: Category;
  ownerId: Owner;
  favoriteCount: number;
  createdAt: string;
  updatedAt: string;
}

export interface StoriesResponse {
  stories: Story[];
  totalStories: number;
  page: number;
  perPage: number;
  totalPages: number;
}

export interface PopularStoriesResponse {
  stories: Story[];
  totalStories: number;
}

export interface PaginationParams {
  page?: number;
  perPage?: number;
}

export interface GetStoriesParams extends PaginationParams {
  category?: string;
}

export interface CreateStoryParams {
  title: string;
  description: string;
  category: string;
  img?: string;
}
export interface UpdateStoryParams {
  title?: string;
  description?: string;
  category?: string;
  img?: string;
}
